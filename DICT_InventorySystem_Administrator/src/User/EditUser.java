    public void displayInfo(){
        
        txtEditFullname.setText(user.getUserFullname());
        txtEditPosition.setText(user.getUserPosition());
        txtEditContactNumber.setText(user.getUserContactNumber());
        txtEditEmail.setText(user.getUserEmail());
        txtEditUsername.setText(user.getUserUsername());
        txtEditPassword.setText(user.getUserPassword());
        
    }
    
    public void accessOption(boolean stat){
        txtEditFullname.setEnabled(stat);
        txtEditPosition.setEnabled(stat);
        txtEditContactNumber.setEnabled(stat);
        txtEditEmail.setEnabled(stat);
        txtEditUsername.setEnabled(stat);
        txtEditPassword.setEnabled(stat);
    }
    
    public User editedUser(){
    
        User editUser= this.user;
        editUser.setUserFullname(txtEditFullname.getText());
        editUser.setUserPosition(txtEditPosition.getText());
        editUser.setUserContactNumber(txtEditContactNumber.getText());
        editUser.setUserEmail(txtEditEmail.getText());
        editUser.setUserUsername(txtEditUsername.getText());
        editUser.setUserPassword(txtEditPassword.getText());        
        return editUser;
        
    }

