    private void sendEmailbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendEmailbtnActionPerformed
        String label = sendEmailbtn.getText();
        
        if(label.equals("Edit")){
            accessOption(true);
            sendEmailbtn.setText("Save");
        }else{
            
            controller = new UserController(editedUser());
            
             
            controller.updateUser();
            
            if(controller.isSuccess()){
                
                CreateSection.displayInfoMessage("Confirmation", 
                        "Successfully Edit User!");
                
                sendEmailbtn.setText("Edit");
                accessOption(false);
                new SetSection().setDisplayFromAbove(this, 70);
                
                
            }else{
                 CreateSection.displayErrorMessage("Edit User Error",
                         "Cannot Edit User!");
            }
            
           
            
        }
     

    }//GEN-LAST:event_sendEmailbtnActionPerformed

