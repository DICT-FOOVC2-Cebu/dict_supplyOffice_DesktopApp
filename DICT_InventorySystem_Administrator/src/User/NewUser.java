    public void createUser(){
        
        String fullname = txtFullname.getText();
        String position = txtPosition.getText();
        String email = txtEmail.getText();
        String contactNumber = txtContactNumber.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        
    
        this.user.setUserFullname(fullname);
        this.user.setUserPosition(position);
        this.user.setUserEmail(email);
        this.user.setUserContactNumber(contactNumber);
        this.user.setUserUsername(username);
        this.user.setUserPassword(password);
        
    }
    public boolean validateInput(){
        
       createUser();
          
       boolean stat = this.user.getUserFullname().isEmpty() ||
               this.user.getUserPosition().isEmpty() ||
               this.user.getUserEmail().isEmpty() || 
               this.user.getUserContactNumber().isEmpty() ||
               this.user.getUserUsername().isEmpty() || 
               this.user.getUserPassword().isEmpty();
       
       
       if(!stat){
           
           this.user.setUserID(CreateSection.generateID(1000000, 2000000, "userID"));
           this.user.setUserCreateDate(CreateSection.getDateToday());
           this.user.setActive(true);
           this.user.setLogged(false);
       }
        
       return stat;
    }
    public void insertProcess(){
        
        if(validateInput()){
            CreateSection.displayErrorMessage("Fields Validation", 
                    "Some fields are empty!");
        }else{
            this.controller = new UserController(this.user);
            this.controller.insertNewUser();
            
            if(this.controller.isSuccess()){
                clearFieldValues();
                CreateSection.displayInfoMessage("Confirmation", 
                        "Successfully Inserted New User");
            }else{
                CreateSection.displayErrorMessage("User Insert Error", 
                        "Failed to Insert New User");
            
            }
        }
    }
    
