    public void loginProcess(){
        
        if(validateInput()){
            
            CreateSection.displayInfoMessage("Fields Validation", "Please Input Empty field/s");
        
        }else{
            controller = new LoginController(adminEntry());
            controller.verifyUser();
            
            if(!controller.isFound()){
                CreateSection.displayInfoMessage("Login Modifier Info", controller.getMessage());
            }else{
                this.setVisible(false);
                new MainPage().setVisible(true);
            }
            
                    
        }
    
    }
    public boolean validateInput(){
        
        String usernameVal = adminTxtUsername.getText();
        String passwordVal = adminTxtPassword.getText();
        
        return usernameVal.isEmpty() || passwordVal.isEmpty();
    }
    
    public Admin adminEntry(){
        
        Admin admin = new Admin();
        admin.setAdminUsername(adminTxtUsername.getText());
        admin.setAdminPassword(adminTxtPassword.getText());
        
        return admin;
    }
  


